<script setup lang="ts">
import type { typeTab } from '@/typescript/enums/enums'

/**
 *khi emit từ component lên thẳng component cha thì khai báo
 *
 * interface Props {
 *    emit?: any
 * }
 * const { emitEvent } = props.emit()
 * emitEvent('name', abc) dùng như emit mặc định của vue
 *
  */
const props = withDefaults(defineProps<Props>(), ({
  listTab: () => ([]),
  type: 'button',
  isSmall: false,
  label: 'tabActive',
  routeName: '',
  hide: false,
  isRender: false,
}))

const emit = defineEmits<Emit>()
const { t } = window.i18n() // Khởi tạo biến đa ngôn ngữ

interface tab {
  key: string
  title?: string
  icon?: string
  component?: any // truyền thẳng component vào nếu dùng composition api, còn không thì truyền string tên component
  dataTab?: any // Dữ liệu riêng của từng tab
  isDisabled?: boolean
}
interface Props {
  hide: boolean
  listTab: tab[]
  type?: typeof typeTab[any]
  isSmall?: boolean
  label?: string
  routeName?: string
  dataGeneral?: any // hạn chế dùng
  isRender: boolean
}
const router = useRouter()
const route = useRoute()

interface Emit {
  (e: string, data: any): void
  (e: 'activeTab', data: tab): void
}
const tabActive = ref<any>({})

const getTabActive = () => {
  if (route.params[props.label] && !tabActive.value[props.label])
    tabActive.value = props.listTab.find(e => e.key === route.params[props.label]) as object
}

getTabActive()

const activeTab = (value: any) => {
  router.push({ name: props.routeName || undefined, params: { [props.label]: value.key } })
  tabActive.value = value
  emit('activeTab', tabActive.value)
}

const useEmitter = () => {
  const emitEvent = (event: any, data: any) => {
    emit(event, data)
  }

  return { emitEvent }
}
</script>

<template>
  <div class="tabs w-100">
    <div class="w-100">
      <VTabs
        v-if="!hide"
        v-model="tabActive"
        :class="`cm-tabs ${type}-tabs`"
        :hide-slider="type === 'button'"
        @update:modelValue="activeTab"
      >
        <VTab
          v-for="(item, index) in listTab"
          :key="index"
          :disabled="item.isDisabled"
          :value="item"
          :class="`item-tab ${tabActive.key === item.key ? 'active' : ''} `"
        >
          <VIcon
            v-if="item.icon"
            :icon="item.icon"
            :size="18"
            class="mr-2"
          />
          <span>{{ t(item.title) }}</span>
        </VTab>
      </VTabs>
    </div>
    <div v-if="props.isRender">
      <div
        v-for="item in listTab"
        :key="item.key"
        class="content-tab"
      >
        <div
          v-show="item.key === route.params[props.label]"
        >
          <Component
            :is="item?.component"
            :emit="useEmitter"
            :data-general="dataGeneral"
            v-bind="tabActive?.dataTab"
          />
        </div>
      </div>
    </div>
    <div v-else>
      <Component
        :is="tabActive?.component"
        :emit="useEmitter"
        :data-general="dataGeneral"
        v-bind="tabActive?.dataTab"
      />
    </div>
  </div>
</template>

